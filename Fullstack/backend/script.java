/*
a poo veio como uma evolução da programação procedural e da programação modular;
a principal evolução foi a possibilidade de reuso, e extensão do código através do polimorfismo e da
herança;
os poos baseiam-se na existencia de objetos e nas mensagens trocados entre ambos;
a instanciação é o momento onde criamos determinado objeto com base em uma classe;
uma classe é composta por um nome, um conjunto de propriedades e um conjunto de métodos também conhecidos 
como operações;
a linguagem de programação java é considerada multiplataforma, pois o seu resultado [os bytecodes]
são interpretados por uma vm que isola as informações do sistema operacional;
dentro da plataforma do java temos:
o JAVASE [usado pra devs de app desktop]
o JAVAEE [usado pra app em ambientes distribuidos e de grande porte]
o JAVAME [versão compatível pra devices menores tais como cartões, telefones, microchips etc]
o JAVAFX [opção fornecida pela oracle pra criações de RIA em java]
o JVM [usa hotspot e o JIT pra otimizar o desempenho de app java, levando a própria linguagem java a ser
mais leve do que C, em determinadas situações]
*/